#include "ft_printf.h"

size_t	ft_putnbr(long long int n)
{
	size_t	i;

	i = 0;
	if (n < 0)
	{
		i += ft_putchar('-');
		n *= -1;
	}
	if (n > 9)
	{
		i += ft_putnbr(n / 10);
		n %= 10;
	}
	if (-1 < n && n < 10)
		i += ft_putchar(n + '0');
	return (i);
}
